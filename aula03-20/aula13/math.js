let numero1 = 10.5;
let numero2;

//arredonda o número pra baixo.
numero2 = Math.floor(numero1);
console.log(numero2);

//arredonda o número pra cima.
numero2 = Math.ceil(numero1);
console.log(numero2);

//arredonda o número pro mais proximo inteiro.
numero2 = Math.round(numero1);
console.log(numero2);

//pega o maior valor dentro os valores passados.
numero1 = Math.max(1, 2, 3, 4, 5, -10, 36);
console.log(numero1);

//pega o menor valor dentro os valores passados.
numero2 = Math.min(-10, -30, 40, 50);
console.log(numero2);

//descobre a raiz quadrada.
numero1 **= 0.5;
console.log(numero1);

